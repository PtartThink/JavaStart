package Class;

public class SE {
    private int id;
    private String name;
    private char gender;
    private double salary;
    private int hot;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public char getGender() {
        return gender;
    }

    public double getSalary() {
        return salary;
    }

    public int getHot() {
        return hot;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public void setHot(int hot) {
        this.hot = hot;
    }



}
