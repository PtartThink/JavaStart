package h1015;

public class SubWay {
    private int number;
    private String company;
    public SubWay() {
        super();
    }
    public SubWay(int number, String company) {
        super();
        this.number = number;
        this.company = company;
    }
    public int getNumber() {
        return number;
    }
    public void setNumber(int number) {
        this.number = number;
    }
    public String getCompany() {
        return company;
    }
    public void setCompany(String company) {
        this.company = company;
    }
}
